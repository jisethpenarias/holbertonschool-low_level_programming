#include "3-calc.h"

/**
 * op_add - the function adds two integers
 * @a: int argument.
 * @b: int argument.
 * Return: operation result add.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - the function sub two integers
 * @a: int argument.
 * @b: int argument.
 * Return: operation result sub.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - the function adds two integers
 * @a: int argument.
 * @b: int argument.
 * Return: operation result mul.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the function adds two integers
 * @a: int argument.
 * @b: int argument.
 * Return: operation result div.
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - the function modulates two integers
 * @a: int argument.
 * @b: int argument.
 * Return: operation result mod.
 */

int op_mod(int a, int b)
{
	return (a % b);
}
